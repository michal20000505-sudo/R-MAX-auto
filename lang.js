let currentLang = localStorage.getItem('site_lang') || 'pl';
let originalTexts = new Map();

function applyTranslation(lang) {
    document.documentElement.lang = lang;

    if (lang !== 'pl') {
        const titleDict = I18N["R-MAX Auto | Serwis Samochodowy Wrocław"];
        if (titleDict) document.title = titleDict[lang] || "R-MAX Auto | Serwis Samochodowy Wrocław";
    } else {
        document.title = "R-MAX Auto | Serwis Samochodowy Wrocław";
    }

    const twalk = document.createTreeWalker(
        document.body,
        NodeFilter.SHOW_TEXT,
        {
            acceptNode: function (n) {
                if (n.parentNode.tagName === 'SCRIPT' || n.parentNode.tagName === 'STYLE') return NodeFilter.FILTER_REJECT;
                if (n.nodeValue.trim() === '') return NodeFilter.FILTER_SKIP;
                return NodeFilter.FILTER_ACCEPT;
            }
        },
        false
    );

    let textNode;
    const nodesToProcess = [];
    while (textNode = twalk.nextNode()) {
        nodesToProcess.push(textNode);
    }

    nodesToProcess.forEach(n => {
        if (!originalTexts.has(n)) {
            originalTexts.set(n, n.nodeValue);
        }

        let val = originalTexts.get(n);
        let trimmed = val.trim();

        if (lang === 'pl') {
            n.nodeValue = val;
            return;
        }

        // Look for match
        let matchKey = Object.keys(I18N).find(k => k === trimmed);
        if (!matchKey) {
            // Attempt substring match
            let temp = val;
            for (const k of Object.keys(I18N)) {
                if (temp.includes(k)) {
                    temp = temp.replace(k, I18N[k][lang] || k);
                }
            }
            n.nodeValue = temp;
        } else {
            n.nodeValue = val.replace(trimmed, I18N[matchKey][lang] || trimmed);
        }
    });
}

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('site_lang', lang);
    applyTranslation(lang);
    updateFlagHighlight();
}

function updateFlagHighlight() {
    document.querySelectorAll('.lang-flag').forEach(f => {
        f.style.opacity = f.dataset.lang === currentLang ? '1' : '0.4';
    });
}

document.addEventListener('DOMContentLoaded', () => {
    const navs = document.querySelectorAll('.navbar, .top-bar');

    navs.forEach(nav => {
        const wrapper = document.createElement('div');
        wrapper.style.display = 'flex';
        wrapper.style.gap = '8px';
        wrapper.style.alignItems = 'center';

        if (nav.classList.contains('top-bar')) {
            wrapper.style.marginLeft = 'auto';
            wrapper.style.marginRight = '20px';
        } else {
            wrapper.style.marginLeft = '1.5rem';
            nav.style.justifyContent = 'flex-end';
            const navLogo = nav.querySelector('.nav-logo');
            if (navLogo) navLogo.style.marginRight = 'auto';
        }
        wrapper.style.cursor = 'default';
        wrapper.style.zIndex = '1000';
        wrapper.classList.add('lang-switcher-wrap');

        const langs = [
            { code: 'pl', img: 'https://flagicons.lipis.dev/flags/4x3/pl.svg' },
            { code: 'en', img: 'https://flagicons.lipis.dev/flags/4x3/gb.svg' },
            { code: 'uk', img: 'https://flagicons.lipis.dev/flags/4x3/ua.svg' }
        ];

        langs.forEach(l => {
            const img = document.createElement('img');
            img.src = l.img;
            img.alt = l.code;
            img.style.width = '24px';
            img.style.height = '18px';
            img.style.cursor = 'pointer';
            img.style.boxShadow = '0 0 3px rgba(0,0,0,0.5)';
            img.style.borderRadius = '3px';
            img.style.transition = 'opacity 0.2s, transform 0.2s';
            img.classList.add('lang-flag');
            img.dataset.lang = l.code;

            img.addEventListener('click', (e) => {
                e.preventDefault();
                setLanguage(l.code);
            });

            img.addEventListener('mouseenter', () => { img.style.transform = 'scale(1.1)'; });
            img.addEventListener('mouseleave', () => { img.style.transform = 'scale(1)'; });

            wrapper.appendChild(img);
        });

        if (nav.classList.contains('top-bar')) {
            nav.insertBefore(wrapper, nav.querySelector('.top-rating'));
        } else {
            // main navbar
            nav.insertBefore(wrapper, nav.querySelector('.hamburger') || null);
        }
    });

    applyTranslation(currentLang);
    updateFlagHighlight();

    const observer = new MutationObserver((mutations) => {
        let run = false;
        mutations.forEach(m => {
            if (m.target.id === 'status-text' || m.target.id === 'status-badge' || m.target.classList.contains('open-tag')) {
                run = true;
            }
        });
        if (run && currentLang !== 'pl') {
            setTimeout(() => applyTranslation(currentLang), 10);
        }
    });

    if (document.getElementById('status-text')) observer.observe(document.getElementById('status-text'), { childList: true, characterData: true, subtree: true });
    if (document.getElementById('status-badge')) observer.observe(document.getElementById('status-badge'), { childList: true, characterData: true, subtree: true });
    if (document.getElementById('open-tag-root')) observer.observe(document.getElementById('open-tag-root'), { childList: true, characterData: true, subtree: true });
});
